    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
    String firstName = txtF1.getText();
    String lastName = txtF2.getText();
    String username = txtF3.getText();
    String email = txtF4.getText();
    String password = new String(PF5.getPassword());
    String ConfirmPassword = new String(PF6.getPassword());
    String gender = rbM.isSelected() ? "Male" : "Female"; // Determine the selected gender
    String selectedCourse = cbCR.getSelectedItem().toString();
    
     StringBuilder hobbies = new StringBuilder("Hobbies: ");
    if (CheckGaming.isSelected()) {
        hobbies.append("Gaming, ");
    }
    if (CheckMusic.isSelected()) {
        hobbies.append("Music, ");
    }
    if (CheckDancing.isSelected()) {
        hobbies.append("Dancing, ");
    }
    if (CheckSinging.isSelected()) {
        hobbies.append("Singing, ");
    }
    if (CheckCooking.isSelected()) {
        hobbies.append("Cooking, ");
    }
    if (CheckCleaning.isSelected()) {
        hobbies.append("Cleaning, ");
    }
    if (hobbies.length() > 6) {
        hobbies.setLength(hobbies.length() - 2);
    }
    if (rbM.isSelected()) {
       gender = "Male";
    } else if (rbFM.isSelected()) {
        gender = "Female";
    } else {
        JOptionPane.showMessageDialog(this, "Please select a gender", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method if no gender is selected
    }
     if (hobbies.length() <= 9) {
        JOptionPane.showMessageDialog(this, "You need to choose at least one hobby", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method if no hobby is selected
    }
    if (firstName.isEmpty() || lastName.isEmpty() || username.isEmpty() || password.isEmpty() || ConfirmPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill out all the fields", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!password.equals(ConfirmPassword)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!email.endsWith("@gmail.com")) {
        JOptionPane.showMessageDialog(this, "Email must end with @gmail.com", "Error", JOptionPane.ERROR_MESSAGE);
    } else{
        try {
            BufferedWriter writer = new BufferedWriter(new FileWriter("user.txt"));

            writer.write("Firstname: " + firstName);
            writer.newLine();
            writer.write("Lastname: " + lastName);
            writer.newLine();
            writer.write("Username: " + username);
            writer.newLine();
            writer.write("Email: " + email);
            writer.newLine();
            writer.write("Password: " + password);
            writer.newLine();
            writer.write("RePassword: " + ConfirmPassword);
            writer.newLine();
            writer.write("Gender: " + gender); // Write the selected gender to the file
            writer.newLine();           
            writer.write("Course: " + selectedCourse);
            writer.newLine();
            writer.write(hobbies.toString()); // Write selected hobbies to the file
            writer.newLine();

            writer.close();
            JOptionPane.showMessageDialog(this, "Thanks for registering!");

        } catch (IOException e) {
            e.printStackTrace();
        }
        LogIn x = new LogIn();
        x.show();
        this.hide();
    }//GEN-LAST:event_btnRegisterActionPerformed
    }
