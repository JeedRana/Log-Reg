    private void cbSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSPActionPerformed
        if (cbSP.isSelected()) {
        // If the checkbox is selected, show the password
        PF5.setEchoChar((char) 0); // Set the echo char to 0 (null)
        PF6.setEchoChar((char) 0);
    } else {
        // If the checkbox is not selected, hide the password
        PF5.setEchoChar('*'); // Set the echo char to '*'
        PF6.setEchoChar('*');
    }//GEN-LAST:event_cbSPActionPerformed
    }    
